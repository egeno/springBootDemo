package com.qjkj.qjcsp.entity.viewmodel;

import java.util.Date;

import com.qjkj.qjcsp.util.DateFormat;

public class CategoryTree {
	private Long id;

	private Long pid;

	private String name;

	private String pName;

	private String iconCls;

	private String code;

	private String state;

	private Short level;

	private String createtime;

	private String modifytime;

	private String modifier;

	private String creater;

	private String isDel;

	private String isLastLayer;
	
	private String status;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getPid() {
		return pid;
	}

	public void setPid(Long pid) {
		this.pid = pid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getpName() {
		return pName;
	}

	public void setpName(String pName) {
		this.pName = pName;
	}

	public String getIconCls() {
		return iconCls;
	}

	public void setIconCls(String iconCls) {
		this.iconCls = iconCls;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public Short getLevel() {
		return level;
	}

	public void setLevel(Short level) {
		this.level = level;
	}

	public String getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = DateFormat.dateFormatYMDHMS(createtime);
	}

	public String getModifytime() {
		return modifytime;
	}

	public void setModifytime(Date modifytime) {
		this.modifytime = DateFormat.dateFormatYMDHMS(modifytime);
	}

	public String getModifier() {
		return modifier;
	}

	public void setModifier(String modifier) {
		this.modifier = modifier;
	}

	public String getCreater() {
		return creater;
	}

	public void setCreater(String creater) {
		this.creater = creater;
	}

	public String getIsDel() {
		return isDel;
	}

	public void setIsDel(String isDel) {
		this.isDel = isDel;
	}

	public String getIsLastLayer() {
		return isLastLayer;
	}

	public void setIsLastLayer(String isLastLayer) {
		this.isLastLayer = isLastLayer;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

}
