package com.qjkj.qjcsp.entity.machinearea;

import java.io.Serializable;

/**
 * 格子信息表
 * @author yehx
 * @date 2015年12月30日  上午11:27:08
 */
public class MachineCell implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long cellId;//格子id
	private Integer machineId;//设备id
	private String deviceCode;//设备硬件id
	private Integer rowNum;//格子所在的行
	private Integer colNum;//格子所在的列
	private String  cellStatus;//格子的状态
	
	public Long getCellId() {
		return cellId;
	}
	public void setCellId(Long cellId) {
		this.cellId = cellId;
	}
	public Integer getMachineId() {
		return machineId;
	}
	public void setMachineId(Integer machineId) {
		this.machineId = machineId;
	}
	public String getDeviceCode() {
		return deviceCode;
	}
	public void setDeviceCode(String deviceCode) {
		this.deviceCode = deviceCode;
	}
	public Integer getRowNum() {
		return rowNum;
	}
	public void setRowNum(Integer rowNum) {
		this.rowNum = rowNum;
	}
	public Integer getColNum() {
		return colNum;
	}
	public void setColNum(Integer colNum) {
		this.colNum = colNum;
	}
	public String getCellStatus() {
		return cellStatus;
	}
	public void setCellStatus(String cellStatus) {
		this.cellStatus = cellStatus;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	@Override
	public String toString() {
		return "MachineCell [cellId=" + cellId + ", machineId=" + machineId + ", deviceCode=" + deviceCode + ", rowNum="
				+ rowNum + ", colNum=" + colNum + ", cellStatus=" + cellStatus + "]";
	}
	
	
}
