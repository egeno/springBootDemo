package com.qjkj.qjcsp.entity.enums;

public enum AreaModelTimeTypeEnum {
	BREAKFAST("早餐", "0"), LUNCH("中餐", "1"), DESSERT("休闲", "2"), DINNER("晚餐", "3");
	private String lable;
	private String value;

	private AreaModelTimeTypeEnum(String lable,String value){
		this.lable = lable ;
		this.value = value ;
	}

	public String getLable() {
		return lable;
	}

	public void setLable(String lable) {
		this.lable = lable;
	}

	public String getValue() {
		return value;
	}
}
